function [xi,wt] = lobatto_quadrature(n,L)

switch n
    case 2
        xi = [-1 1];
        wt = [1 1];
    case 3
        xi = [-1 0 1];
        wt = [1/3 4/3 1/3];
    case 4
        xi = [-1 -sqrt(5)/5 sqrt(5)/5 1];
        wt = [1/6 5/6 5/6 1/6];
    case 5
        xi = [-1 -sqrt(21)/7 0 sqrt(21)/7 1];
        wt = [1/10 49/90 32/45 49/90 1/10];
    case 6
        xi = [-1.0 -0.7650553239 -0.2852315164 ...
            0.2852315164 0.7650553239 1.0];
        wt = [0.06666666667 0.3784749562 0.5548583770 ...
            0.5548583770 0.3784749562 0.06666666667];
    case 7
        xi = [-1.0 -0.8302238962 -0.4688487934 ...
            0.0 0.4688487934 0.8302238962 1.0];
        wt = [0.04761904762 0.2768260473 0.4317453812 ...
            0.4876190476 0.4317453812 0.2768260473 ...
            0.04761904762];
    case 8
        xi = [-1.0 -0.8717401485 -0.5917001814 ...
            -0.2092992179 0.2092992179 0.5917001814 ...
            0.8717401485 1.0];
        wt = [0.03571428571 0.2107042271 0.3411226924 ...
            0.4124587946 0.4124587946 0.3411226924 ...
            0.2107042271 0.03571428571];
    case 9
        xi = [-1.0 -0.8997579954 -0.6771862795 ...
            -0.3631174638 0.0 0.3631174638 ...
            0.6771862795 0.8997579954 1.0];
        wt = [0.02777777778 0.1654953615 0.2745387125 ...
            0.3464285109 0.3715192743 0.3464285109 ...
            0.2745387125 0.1654953615 0.02777777778];
    case 10
        xi = [-1.0 -0.9195339082 -0.7387738651 ...
            -0.4779249498 -0.1652789577 0.1652789577 ...
            0.4779249498  0.7387738651 0.9195339082 1.0];
        wt = [0.02222222222 0.1333059908 0.2248893421 ...
            0.2920426836 0.3275397611 0.3275397611 ...
            0.2920426836 0.2248893421 0.1333059908 ...
            0.02222222222];
    otherwise
        [xi,wt,~]=lglnodes(n-1);
        %error('Lobatto quadrature not implemented for n = %i',n);
end

% Adjust the range of integration
if nargin > 1
    xi = 0.5*L*(xi+1.0);
    wt = 0.5*L*wt;
end

end